// ? variable naming convention

// 1. variable name must not contain reserved keywords
// e.g. for, while, switch, let, const, break, do, function etc...

// 2. variable name must not start with numbers

// !let 2number = 5; // wrong
// let number2 = 10; // right

// 3. variable name must not contain special characters
//  such as  @ ,#, %, +, - etc. except $ and _

// !let name# =2  //wrong

// let user_name = 'John'; //right

// let price$ = 25; //right

// let _weight = 60; //right

// let _ = 5; //right

// let $ = 'Nepal'; //right

// 4. variable name should be descriptive
// t means temperature
// let t = 25;

// let temperature = 25; // right approach
// console.log(25);

// let country = 'Nepal';

// 5. variable name should be camelCase

